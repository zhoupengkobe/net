package com.kobe.net.tcp.chatdemo02;

import java.io.Closeable;

/**
 * 关闭流的方法
 * @author ko
 *
 */
public class CloseUtil {

	public static void closeAll(Closeable... io){
		for (Closeable temp : io) {
			try {
				if (null != temp) {
					temp.close();
				} 
			} catch (Exception e) {
				
			}
		}
	}

}
